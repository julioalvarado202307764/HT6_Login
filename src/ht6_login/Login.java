    private Map<String, String> leerUsuarios() throws IOException {
    Map<String, String> usuarios = new HashMap<>();

    String ruta = new File("usuarios.txt").getAbsolutePath();
    File archivo = new File(ruta);
    if (archivo.canRead()) {
        BufferedReader lector = new BufferedReader(new FileReader(archivo));
        String linea;
        while ((linea = lector.readLine()) != null) {
            String[] partes = linea.split(",");
            if (partes.length == 2) {
                String usuario = partes[0].trim();
                String contrasena = partes[1].trim();
                usuarios.put(usuario, contrasena);
            }
        }
        lector.close();
    } else {
        JOptionPane.showMessageDialog(this, "El programa no tiene permisos de lectura en el archivo de usuarios");
    }
    return usuarios;
}

    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        String usuario = txtUsuario.getText();
        char[] contrasena = txtPass.getPassword();

        if (usuario.isEmpty() || contrasena.length == 0) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese usuario y contrase침a", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            try {
                Map<String, String> usuarios = leerUsuarios();
                if (usuarios.containsKey(usuario) && usuarios.get(usuario).equals(new String(contrasena))) {
                    EditorTexto editor = new EditorTexto();
                    editor.setVisible(true);
                    editor.setResizable(false);
                    editor.setLocationRelativeTo(null);
                } else {
                    // Credenciales inv치lidas
                    JOptionPane.showMessageDialog(this, "Usuario o contrase침a incorrectos");
                }
            } catch (IOException ex) {
                // Error al leer el archivo de 
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al leer el archivo de usuarios", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnIniciarActionPerformed

